package Java_27_July_Task;

public class Print_100_To_81_Using_Do_While_Loop {
	
	// This Program Is Related To " PRINTING 100 TO 81 USING DO WHILE LOOP ".

	public static void main ( String args[] ) {
		
		int i = 100 ;
		
		System.out.println("Printing 100 To 81 Using Do While Loop :\n") ;
		
		do {
			
			System.out.println (i);
			i-- ;
		}
		
		while (i>=81) ;
	}	
}