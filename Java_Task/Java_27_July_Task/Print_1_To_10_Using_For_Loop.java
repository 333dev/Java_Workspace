package Java_27_July_Task;

public class Print_1_To_10_Using_For_Loop {
	
	// This Program Is Related To " PRINTING 1 TO 10 USING FOR LOOP ".

	public static void main (String args[]) {
		
		int i = 1 ;
		
		System.out.println("Printing 1 To 10 Using For Loop :\n") ;
		
		for( i= 1; i<=10 ; i++) {
			
			System.out.println (i) ;
		}	
	}
}