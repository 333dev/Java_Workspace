package Java_27_July_Task;

public class Print_51_To_60_Using_While_Loop {

	public static void main ( String args[] ) {
		
		// This Program Is Related To " PRINTING 51 TO 60 USING WHILE LOOP ".
		
		int i = 51 ;
		
		System.out.println("Printing 51 To 60 Using While Loop :\n") ;
		
		while (i<=60) {
			
			System.out.println(i);
			i++ ;
		}
	}	
}