package Java_1_August_Task;

public class Natural_Numbers {
	
	//This Program Is Related To "Natural Number" . 

	public static void main(String args[]) {
		
		int n = 1;
		
		System.out.println("According To While Loop First 10 Natural Number Is :\n");
		
		while (n<=10) {
			
			System.out.println(n);
			n++;	
		}
		
	}
}