package Java_25_July_Task;

public class Simple_Intrest {

	public static void main (String args[]) {
		
		int Principle_Amount = 1000 ;
		int Rate_of_Intrest = 10 ;
		int Term = 2 ;
		int Simple_Intrest = Principle_Amount * Rate_of_Intrest * Term / 100 ;
		
		System.out.println ("Simple Intrest is : " + Simple_Intrest) ; 	
	}	
}

/* Formula For Calculating Simnple Intrest Is 

P*R*T / 100 Whereas, 

P = Principle Amount;
R = Rate Of Intrest;
T = Term ;

*/