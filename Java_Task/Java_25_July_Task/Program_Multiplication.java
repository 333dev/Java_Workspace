package Java_25_July_Task;

public class Program_Multiplication {

	public static void main (String args[]) { 
		
		int a = 5 ; 
		int b = 5 ;
		int c = a * b ;
		int d = a + b ; 
		int e = a - b ;
		int f = a / b ;
		
		System.out.println ("Different Calculation Of a & b ");
		System.out.println ();
		
		System.out.println ("Multiplication Of a & b is :" + c);
		System.out.println ("Addition Of a & b is :" + d);
		System.out.println ("Substraction Of a & b :" + e);
		System.out.println ("Division Of a & b :" + f);
	}
		
}
