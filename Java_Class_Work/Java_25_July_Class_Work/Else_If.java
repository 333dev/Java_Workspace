package Java_Data_Type_Class_Work;

public class Else_If {
	
	public static void main (String args[]) {
		
		int marks = 99;
		
		if (marks >= 80) {
			
			System.out.println("Grade A");
		}
		
		else if (marks >= 60) {
			
			System.out.println ("Grade B");
		}
		
		else if (marks >= 40) {
			
			System.out.println ("Grade C");
		}
		
		else {
			
			System.out.println ("Grade D");
		}
		
	}

}
