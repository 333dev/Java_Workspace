package Java_Data_Type_Class_Work;

public class Switch_Case {
	
	public static void main (String args[]) {
		
		int number = 3 ;
		
		switch (number) {
		
			case 1 : System.out.println ("Hindi Language") ;
			break;
			
			case 2 : System.out.println ("Gujarati Language") ;
			break;
			
			case 3 : System.out.println ("English Language") ;
			break;
			
			default : System.out.println ("Please Enter Correct Input");
			break;		
		}
		
	}

}