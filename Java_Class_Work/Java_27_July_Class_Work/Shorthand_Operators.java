package Java_27_July_Class_Work;

public class Shorthand_Operators {
	
	// This Program Is Related To " SHORT HAND OPERATORS ".
	
	public static void main ( String args[] ) {
		
		int A = 5 ;
		int B = 3 ; 
		
		System.out.println (A += B ) ; // A = A + B
		System.out.println (A -= B ) ; // A = A - B 
		System.out.println (B -= A ) ; // B = B - A ( 3 - 8 ) 
		System.out.println (A -= B ) ; // A = A - B (-8) - (-5) 		
	
	}
}