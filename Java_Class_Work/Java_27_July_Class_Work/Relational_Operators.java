package Java_27_July_Class_Work;

public class Relational_Operators {

	// This Program Is Related To " RELATIONAL OPERATORS ".
	
	public static void main ( String args [] ) {
		
		int A = 6 ;
		int B = 9 ;
		
		System.out.println (A>B) ;
		System.out.println (A<B) ;
		System.out.println (A>=B) ;
		System.out.println (A<=B) ;
		System.out.println (A==B) ;
		System.out.println (A!=B) ;
		
	}
}