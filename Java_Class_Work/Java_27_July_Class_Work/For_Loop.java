package Java_27_July_Class_Work;

public class For_Loop {
	
	//  This Program Is Related To " FOR LOOP ".
	
	public static void main ( String args [] ) {
		
		int i = 1 ;
		
		for ( i = 1; i<=10 ; i++ ) {
			
			System.out.println (i) ;
		}		
	}
}

